import fs from "node:fs";
import path from "node:path";

const assetsDir = path.resolve("src/assets");
const outPath = path.resolve("src/lib/email-assets.ts");

const images = [
  { key: "logo", file: "logo.png", mime: "image/png" },
  { key: "seal", file: "seal.jpg", mime: "image/jpeg" },
  { key: "msme", file: "msme.png", mime: "image/png" },
  { key: "sigFounder", file: "sig-founder.jpg", mime: "image/jpeg" },
  { key: "sigCofounder", file: "sig-cofounder.jpeg", mime: "image/jpeg" },
];

const lines = ["// AUTO-GENERATED by scripts/generate-email-assets.mjs — DO NOT EDIT", ""];
lines.push("export const EMAIL_ASSETS: Record<string, string> = {");
for (const img of images) {
  const filePath = path.join(assetsDir, img.file);
  if (!fs.existsSync(filePath)) {
    console.error(`[generate-email-assets] MISSING: ${filePath}`);
    process.exit(1);
  }
  const buffer = fs.readFileSync(filePath);
  const b64 = buffer.toString("base64");
  const dataUri = `data:${img.mime};base64,${b64}`;
  lines.push(`  ${img.key}: "${dataUri}",`);
  console.log(`[generate-email-assets] ${img.key}: ${img.file} (${buffer.length} bytes)`);
}
lines.push("};");
lines.push("");

fs.writeFileSync(outPath, lines.join("\n"), "utf-8");
console.log(`[generate-email-assets] Wrote ${outPath}`);
